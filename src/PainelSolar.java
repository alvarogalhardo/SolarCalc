public class PainelSolar {
    private int codPainel;
    private int codMedidorPainel;
    private int codCadastro;
    private String modelo;
    private float potencia;
    private float energiaProduzida;
    private float eficiencia;

    public int getCodPainel() {
        return codPainel;
    }
    public void setCodPainel(int codPainel) {
        this.codPainel = codPainel;
    }
    public int getCodMedidorPainel() {
        return codMedidorPainel;
    }
    public void setCodMedidorPainel(int codMedidorPainel) {
        this.codMedidorPainel = codMedidorPainel;
    }
    public int getCodCadastro() {
        return codCadastro;
    }
    public void setCodCadastro(int codCadastro) {
        this.codCadastro = codCadastro;
    }
    public String getModelo() {
        return modelo;
    }
    public void setModelo(String modelo) {
        this.modelo = modelo;
    }
    public float getPotencia() {
        return potencia;
    }
    public void setPotencia(float potencia) {
        this.potencia = potencia;
    }
    public float getEnergiaProduzida() {
        return energiaProduzida;
    }
    public void setEnergiaProduzida(float energiaProduzida) {
        this.energiaProduzida = energiaProduzida;
    }
    public float getEficiencia() {
        return eficiencia;
    }
    public void setEficiencia(float eficiencia) {
        this.eficiencia = eficiencia;
    }

}
